#include "libft.h"

char		**ft_mvtab(char **tab, int i)
{
	while (tab[i])
	{
		tab[i][ft_strlen(tab[i] + 1) + 1] = '\0';
		tab[i] = ft_strdup(tab[i + 1]);
		i++;
	}
	return (tab);
}
