int			ft_atoi(const char *str)
{
	int		i;
	int		nb;
	int		op;

	i = 0;
	nb = 0;
	op = 1;
	while (str[i] >= 1 && str[i] <= 32)
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			op = op * -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
		nb = (nb * 10) + (str[i++] - 48);
	return (nb * op);
}
